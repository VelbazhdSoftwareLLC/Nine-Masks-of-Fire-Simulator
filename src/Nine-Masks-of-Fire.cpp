void spin(int **const reels, int length) {
	for (int i = 0, u, m, d; i < 5; i++) {
		u = rand() % length;
		m = u + 1;
		d = u + 2;

		m = m % length;
		d = d % length;

		view[i][0] = reels[i][u];
		view[i][1] = reels[i][m];
		view[i][2] = reels[i][d];
	}
}

